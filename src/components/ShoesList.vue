  import { mapActions } from 'vuex';
  import { updateToken } from '../store/local_storage';
  import cartQtu from '../mixins/cartQtu';
  import imageUrl from '../mixins/imageUrl';

  export default {
    name: 'ShoesList',

    mixins: [
      cartQtu,
      imageUrl
    ],

    computed: {
      totalAmount: function () {
        return this.getCart.reduce((a, {qtu, price}) => 
          Math.round(((a + (qtu * price)) + Number.EPSILON) * 100) / 100, 0);
      },
    },

    data() {
      return {
        idx: undefined
      }
    },

    methods: {
      ...mapActions([ 'fetchCart',
                      'initialState',
                      'setLoadingState' ]),

      updateQtu(item, type) {
        this.fetchCart(updateToken(item, type));
      },

      hoverDel(idx) {
        this.idx = idx;
      },
    },

    async mounted() {
      await this.initialState();
    }
  }
